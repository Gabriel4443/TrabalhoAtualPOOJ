package models;

public class Taxas {
	//Atributos 
	private float atraso;
	private float danificado;
	
	// Set para atraso
    public void setAtraso(float atraso) {
        this.atraso = atraso;
    }

    // Get para atraso
    public float getAtraso() {
        return atraso;
    }

    // Set para danificado
    public void setDanificado(float danificado) {
        this.danificado = danificado;
    }

    // Get para danificado
    public float getDanificado() {
        return danificado;
    }
}
